"""Comprehensive markdown report generator for QA feature flag evaluation.

Produces a feature-by-metric matrix plus detailed per-feature findings.
"""

from __future__ import annotations

from evaluation.schemas import (
    ComboEvalResult,
    FeatureFinding,
    FlagImpactSummary,
    FlagRunMetrics,
    IngestionFeatureTest,
    QARecommendation,
    QAReport,
    QueryEvalResult,
    StandaloneFeatureTest,
)


def generate_qa_report(report: QAReport) -> str:
    """Generate a comprehensive markdown evaluation report."""
    lines: list[str] = []

    # Header
    lines.append("# NEXUS Feature Flag QA Evaluation Report")
    lines.append("")
    lines.append(f"**Generated**: {report.timestamp.strftime('%Y-%m-%d %H:%M:%S UTC')}")
    lines.append("")

    # Environment
    _add_environment_section(lines, report)

    # Baseline results
    if report.baseline:
        _add_baseline_section(lines, report.baseline)

    # Individual feature matrix
    if report.individual_results:
        _add_individual_matrix(lines, report.individual_results, report.individual_query_details)

    # Combination matrix
    if report.combo_results:
        _add_combo_matrix(lines, report.combo_results)

    # Standalone feature results
    if report.standalone_results:
        _add_standalone_section(lines, report.standalone_results)

    # Ingestion feature results
    if report.ingestion_results:
        _add_ingestion_section(lines, report.ingestion_results)

    # Findings (bugs & issues)
    if report.findings:
        _add_findings_section(lines, report.findings)

    # Recommendations
    if report.recommendations:
        _add_recommendations_section(lines, report.recommendations)

    lines.append("")
    lines.append("---")
    lines.append("*Report generated by NEXUS QA Evaluation Framework*")
    return "\n".join(lines)


def _add_environment_section(lines: list[str], report: QAReport) -> None:
    lines.append("## Environment")
    lines.append("")
    if report.environment:
        for key, value in report.environment.items():
            lines.append(f"- **{key}**: {value}")
    else:
        lines.append("- No environment metadata captured")
    lines.append("")


def _add_baseline_section(lines: list[str], baseline: ComboEvalResult) -> None:
    lines.append("## Baseline Results")
    lines.append("")
    lines.append("Default configuration (all flags at their default state).")
    lines.append("")
    _add_metrics_table(lines, "Baseline", baseline.metrics)
    if baseline.judge_composite > 0:
        lines.append(f"- **Judge Composite**: {baseline.judge_composite:.2f} / 5.0")
    lines.append("")


def _add_metrics_table(lines: list[str], label: str, metrics: FlagRunMetrics) -> None:
    lines.append(f"| Metric | {label} |")
    lines.append("|--------|--------|")
    lines.append(f"| Latency p50 | {metrics.latency_p50_ms:.0f} ms |")
    lines.append(f"| Latency p95 | {metrics.latency_p95_ms:.0f} ms |")
    lines.append(f"| Latency mean | {metrics.latency_mean_ms:.0f} ms |")
    if metrics.retrieval:
        lines.append(f"| MRR@10 | {metrics.retrieval.mrr_at_10:.4f} |")
        lines.append(f"| Recall@10 | {metrics.retrieval.recall_at_10:.4f} |")
        lines.append(f"| NDCG@10 | {metrics.retrieval.ndcg_at_10:.4f} |")
    if metrics.citation:
        lines.append(f"| Citation Accuracy | {metrics.citation.citation_accuracy:.4f} |")
        lines.append(f"| Hallucination Rate | {metrics.citation.hallucination_rate:.4f} |")
        lines.append(f"| Total Claims | {metrics.citation.total_claims} |")
        lines.append(f"| Claim Extraction Rate | {metrics.citation.claim_extraction_rate:.2%} |")
    lines.append(f"| Queries | {metrics.num_queries} |")
    lines.append(f"| Gates Passed | {'Yes' if metrics.quality_gates_passed else 'No'} |")
    lines.append("")


def _add_individual_matrix(
    lines: list[str],
    impacts: list[FlagImpactSummary],
    query_details: dict[str, list[QueryEvalResult]],
) -> None:
    lines.append("## Individual Feature Flag Results")
    lines.append("")
    lines.append("Each row shows the delta when the flag is toggled from its default state.")
    lines.append("")

    # Header
    lines.append(
        "| Flag | MRR Delta | Recall Delta | NDCG Delta | Latency Delta (ms) "
        "| Citation Delta | Judge Delta | Recommendation |"
    )
    lines.append(
        "|------|-----------|-------------|------------|-------------------"
        "|----------------|-------------|----------------|"
    )

    for impact in impacts:
        judge_delta = getattr(impact, "delta_judge_composite", 0.0) or 0.0
        lines.append(
            f"| `{impact.flag_name}` "
            f"| {impact.delta_mrr:+.4f} "
            f"| {impact.delta_recall:+.4f} "
            f"| {impact.delta_ndcg:+.4f} "
            f"| {impact.delta_latency_mean_ms:+.0f} "
            f"| {impact.delta_citation_accuracy:+.4f} "
            f"| {judge_delta:+.2f} "
            f"| {impact.recommendation.value} |"
        )
    lines.append("")

    # Per-flag detail sections
    for impact in impacts:
        flag = impact.flag_name
        details = query_details.get(flag, [])
        if not details:
            continue
        lines.append(f"### {flag}")
        lines.append("")
        errors = [d for d in details if d.error]
        if errors:
            lines.append(f"**Errors**: {len(errors)}/{len(details)} queries failed")
            for e in errors[:3]:
                lines.append(f"  - `{e.query_id}`: {e.error}")
        avg_judge = _avg_judge(details)
        if avg_judge is not None:
            lines.append(f"**Avg Judge Score**: {avg_judge:.2f}/5.0")
        lines.append("")


def _add_combo_matrix(lines: list[str], combos: list[ComboEvalResult]) -> None:
    lines.append("## Curated Combination Results")
    lines.append("")
    lines.append("| Combo | Latency p50 | MRR@10 | Recall@10 | NDCG@10 | Judge Composite | Queries |")
    lines.append("|-------|-------------|--------|-----------|--------|-----------------|---------|")
    for combo in combos:
        m = combo.metrics
        mrr = m.retrieval.mrr_at_10 if m.retrieval else 0.0
        recall = m.retrieval.recall_at_10 if m.retrieval else 0.0
        ndcg = m.retrieval.ndcg_at_10 if m.retrieval else 0.0
        lines.append(
            f"| {combo.name} "
            f"| {m.latency_p50_ms:.0f} ms "
            f"| {mrr:.4f} "
            f"| {recall:.4f} "
            f"| {ndcg:.4f} "
            f"| {combo.judge_composite:.2f} "
            f"| {m.num_queries} |"
        )
    lines.append("")


def _add_standalone_section(lines: list[str], tests: list[StandaloneFeatureTest]) -> None:
    lines.append("## Standalone Feature Tests")
    lines.append("")
    lines.append("| Flag | Endpoint | Status | Latency | Error |")
    lines.append("|------|----------|--------|---------|-------|")
    for t in tests:
        status = "PASS" if t.functional else "FAIL"
        error = t.error or "-"
        lines.append(f"| `{t.flag_name}` | `{t.endpoint}` | {status} | {t.latency_ms:.0f} ms | {error} |")
    lines.append("")
    for t in tests:
        if t.notes:
            lines.append(f"**{t.flag_name}**: {t.notes}")
    lines.append("")


def _add_ingestion_section(lines: list[str], tests: list[IngestionFeatureTest]) -> None:
    lines.append("## Ingestion Feature Tests")
    lines.append("")
    lines.append("| Flag | Docs Ingested | Ingestion Time | Error |")
    lines.append("|------|---------------|----------------|-------|")
    for t in tests:
        error = t.error or "-"
        lines.append(f"| `{t.flag_name}` | {t.docs_ingested} | {t.ingestion_latency_ms:.0f} ms | {error} |")
    lines.append("")


def _add_findings_section(lines: list[str], findings: list[FeatureFinding]) -> None:
    lines.append("## Findings & Issues")
    lines.append("")

    by_severity: dict[str, list[FeatureFinding]] = {}
    for f in findings:
        by_severity.setdefault(f.severity, []).append(f)

    for severity in ["critical", "high", "medium", "low", "info"]:
        items = by_severity.get(severity, [])
        if not items:
            continue
        lines.append(f"### {severity.upper()}")
        lines.append("")
        for f in items:
            lines.append(f"- **{f.flag_name}** ({f.category}): {f.description}")
            if f.evidence:
                lines.append(f"  - Evidence: {f.evidence}")
        lines.append("")


def _add_recommendations_section(lines: list[str], recommendations: dict[str, QARecommendation]) -> None:
    lines.append("## Recommendations")
    lines.append("")

    by_rec: dict[str, list[str]] = {}
    for flag, rec in recommendations.items():
        by_rec.setdefault(rec.value, []).append(flag)

    for label, desc in [
        ("enable", "Recommended to enable (quality improvement with acceptable cost)"),
        ("fix_first", "Has issues that need fixing before enabling"),
        ("skip", "Not recommended (regression or unacceptable latency cost)"),
        ("neutral", "No significant impact — enable or disable based on preference"),
    ]:
        flags = by_rec.get(label, [])
        if not flags:
            continue
        lines.append(f"### {label.upper()}: {desc}")
        lines.append("")
        for f in flags:
            lines.append(f"- `{f}`")
        lines.append("")


def _avg_judge(results: list[QueryEvalResult]) -> float | None:
    """Average judge composite across query results that have scores."""
    scores = [r.judge_score.composite for r in results if r.judge_score]
    return sum(scores) / len(scores) if scores else None


def compute_recommendation(
    *,
    functional: bool,
    error_rate: float,
    judge_composite_delta: float,
    retrieval_delta: float,
    latency_delta_s: float,
) -> QARecommendation:
    """Compute a recommendation for a feature flag based on evaluation metrics."""
    if not functional or error_rate > 0.20:
        return QARecommendation.FIX_FIRST
    if judge_composite_delta > 0.2 or (retrieval_delta > 0.02 and latency_delta_s < 5.0):
        return QARecommendation.ENABLE
    if judge_composite_delta < 0 or (abs(judge_composite_delta) < 0.1 and latency_delta_s > 3.0):
        return QARecommendation.SKIP
    return QARecommendation.NEUTRAL
